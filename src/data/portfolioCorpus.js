// 辅助方法：由于原 projects.js 中的 id 是 y1p1 到 y5p5，此处通过工具方法将其转译或直接根据 id 搜索。
export function getPortfolioData(projectId) {
    if (!projectId) return {
        text: '这是一份缺失记载的项目蓝本。',
        params: { siteArea: '-', gfa: '-', far: '-', tags: '草稿' }
    };

    // Convert 'y1p1' to 'A1', 'y2p2' to 'B2'
    const yearFormatMap = { 'y1': 'A', 'y2': 'B', 'y3': 'C', 'y4': 'D', 'y5': 'E' };
    const prefix = projectId.substring(0, 2);
    const suffix = projectId.substring(3);

    const mappedKey = `${yearFormatMap[prefix] || 'A'}${suffix}`;

    return portfolioCorpus.projectDescriptions[mappedKey] || {
        text: '一份神秘的遗失图纸，记载着不可名状的空间实验。',
        params: { siteArea: '保密', gfa: '保密', far: '保密', tags: '未知异构' }
    };
}

// 获取导师专属评语（S级/卓越 或 A级/优秀）
export function getTutorPortfolioComment(tutorId, gradeString) {
    const comments = portfolioCorpus.instructorComments[tutorId];
    if (!comments) return (gradeString === 'S' || gradeString === 'A') ? '令人震撼的顶级排版与空间解构能力。' : '十分踏实稳定的图纸推进，值得鼓励。';

    if (gradeString === 'S') return comments['A'];
    if (gradeString === 'A') return comments['B'];
    return comments['B'];
}
